export function normalizeOperationalText(value) {
  return String(value || "")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "");
}

function normalizedState(value) {
  const normalized = normalizeOperationalText(value);
  return stateAliases.get(normalized) || normalized;
}

function distanceKm(aLat, aLng, bLat, bLng) {
  const toRad = (value) => (Number(value) * Math.PI) / 180;
  const earthKm = 6371;
  const dLat = toRad(bLat - aLat);
  const dLng = toRad(bLng - aLng);
  const lat1 = toRad(aLat);
  const lat2 = toRad(bLat);
  const a = Math.sin(dLat / 2) ** 2 + Math.cos(lat1) * Math.cos(lat2) * Math.sin(dLng / 2) ** 2;
  return 2 * earthKm * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
}

function resourceText(resource) {
  return [
    resource.name,
    resource.fullName,
    resource.state,
    resource.municipality,
    resource.parish,
    resource.zone,
    resource.publicLocation,
    resource.hospitalName,
    resource.affectedZone?.state,
    resource.affectedZone?.municipality,
    resource.affectedZone?.parish,
    resource.affectedZone?.sector,
  ].filter(Boolean).join(" ");
}

export function classifyOperationalResource(recordType) {
  if (recordType === "hospital" || recordType === "hospitalized_person") return "hospital_near_disaster";
  if (recordType === "shelter") return "shelter";
  if (recordType === "collection_center") return "collection_center";
  if (["help_center", "water_point", "food_point", "medical_point", "pet_aid_center", "logistics_center"].includes(recordType)) return recordType;
  return "help_center";
}

export function findAffectedOperationalZone(resource = {}) {
  const state = normalizedState(resource.state || resource.affectedZone?.state);
  const municipality = normalizeOperationalText(resource.municipality || resource.affectedZone?.municipality);
  const haystack = normalizeOperationalText(resourceText(resource));
  const lat = Number(resource.latitudePrivate ?? resource.lat ?? resource.approximateLat ?? resource.affectedZone?.approximateLat ?? resource.affectedZone?.lat);
  const lng = Number(resource.longitudePrivate ?? resource.lng ?? resource.approximateLng ?? resource.affectedZone?.approximateLng ?? resource.affectedZone?.lng);

  return affectedOperationalZones.find((zone) => {
    const zoneState = normalizedState(zone.state);
    const zoneMunicipality = normalizeOperationalText(zone.municipality);
    const zoneSector = normalizeOperationalText(zone.sector);
    const stateMatches = state && state === zoneState;
    const placeMatches = haystack.includes(zoneMunicipality) || haystack.includes(zoneSector) || (municipality && municipality === zoneMunicipality);
    if (Number.isFinite(lat) && Number.isFinite(lng) && distanceKm(lat, lng, zone.approximateLat, zone.approximateLng) <= zone.radiusKm) return true;
    return stateMatches && placeMatches;
  });
}

export function isInAffectedOperationalZone(resource) {
  return Boolean(findAffectedOperationalZone(resource));
}
